package Strings;

public class PrintNFloat {
    public static void main(String[] args){
        double num = 27.992638970;
        System.out.println("4 no. after decimal: "+ String.format("%.4f",num));
        System.out.println("7 decimals: " + String.format("%.7f", num));
        System.out.println("2 decimals: " + String.format("%.2f", num));
    }
    
}
